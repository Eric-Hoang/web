     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Card::insert(collect()->times($request->amount, function () use ($request) {
            return [
                'number' => mt_rand(pow(10, 10), pow(10, 16)),
                'seri' => mt_rand(pow(10, 10), pow(10, 16)),
                'amount' => $request->price,
                'created_at' => now(),
                'updated_at' => now(),
            ];
        })->toArray());

        return back()->with(['message' => 'Cards created.']);
    }

